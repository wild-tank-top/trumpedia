/**
 * 質問タイトルから「核心キーワード（一言）」を抽出する
 * 最大10文字で返す
 */
export function extractKeyword(title: string): string {
  // 末尾の句読点・疑問符を除去
  let t = title.trim().replace(/[？?！!。、…\.]+$/, "").trim();

  // よくある文末表現を除去（長いものから順に試す）
  const endings = [
    "って結局何をすればいいの",
    "って何をすればいいの",
    "の方法を教えてください",
    "を教えてください",
    "の方法を教えて",
    "はどうすればいいですか",
    "はどうすれば",
    "はどうやっていますか",
    "はどうやって",
    "することはできますか",
    "することができますか",
    "ことはできますか",
    "ことができますか",
    "でしょうか",
    "すべきですか",
    "すればいいですか",
    "すればいいの",
    "をするには",
    "するには",
    "ありますか",
    "できますか",
    "ますか",
    "ですか",
    "ですよね",
    "ますよね",
    "のですが",
  ];

  for (const ending of endings) {
    const idx = t.indexOf(ending);
    if (idx > 1) {
      t = t.slice(0, idx);
      break;
    }
  }

  // 助詞の直前にある名詞句を抽出（2〜10文字の最短一致）
  const m = t.match(/^(.{2,10}?)(?:[はがをにでもとから]|って)/);
  if (m) {
    t = m[1];
  }

  t = t.trim().slice(0, 10);
  return t.length >= 2 ? t : title.slice(0, 8);
}
